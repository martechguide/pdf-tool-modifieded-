<script>
document.addEventListener('DOMContentLoaded', function() {
    // Desktop dropdown functionality
    const dropdowns = document.querySelectorAll('.dropdown');

    dropdowns.forEach(function(dropdown) {
        const toggle = dropdown.querySelector('.dropdown-toggle');
        const menu = dropdown.querySelector('.dropdown-menu');

        if (toggle && menu) {
            // Desktop hover functionality
            if (window.innerWidth > 768) {
                dropdown.addEventListener('mouseenter', function() {
                    dropdown.classList.add('active');
                });

                dropdown.addEventListener('mouseleave', function() {
                    dropdown.classList.remove('active');
                });
            }
        }
    });

    // Mobile menu functionality
    const mobileMenuBtn = document.getElementById('mobileMenuBtn');
    const mobileMenuPanel = document.getElementById('mobileMenuPanel');
    const mobileMenuOverlay = document.getElementById('mobileMenuOverlay');
    const mobileMenuClose = document.getElementById('mobileMenuClose');

    function openMobileMenu() {
        mobileMenuBtn.classList.add('active');
        mobileMenuPanel.classList.add('active');
        mobileMenuOverlay.classList.add('active');
        document.body.classList.add('mobile-menu-open');
    }

    function closeMobileMenu() {
        mobileMenuBtn.classList.remove('active');
        mobileMenuPanel.classList.remove('active');
        mobileMenuOverlay.classList.remove('active');
        document.body.classList.remove('mobile-menu-open');

        // Close all open sections
        const activeSections = document.querySelectorAll('.mobile-section-toggle.active');
        const activeContents = document.querySelectorAll('.mobile-section-content.active');

        activeSections.forEach(section => section.classList.remove('active'));
        activeContents.forEach(content => content.classList.remove('active'));
    }

    if (mobileMenuBtn) {
        mobileMenuBtn.addEventListener('click', function(e) {
            e.preventDefault();
            e.stopPropagation();

            if (mobileMenuPanel.classList.contains('active')) {
                closeMobileMenu();
            } else {
                openMobileMenu();
            }
        });
    }

    if (mobileMenuClose) {
        mobileMenuClose.addEventListener('click', closeMobileMenu);
    }

    if (mobileMenuOverlay) {
        mobileMenuOverlay.addEventListener('click', closeMobileMenu);
    }

    // Mobile section toggle functionality
    const sectionToggles = document.querySelectorAll('.mobile-section-toggle');

    sectionToggles.forEach(function(toggle) {
        toggle.addEventListener('click', function(e) {
            e.preventDefault();

            const section = toggle.getAttribute('data-section');
            const content = document.getElementById(section + '-content');

            if (content) {
                const isActive = toggle.classList.contains('active');

                // Close all other sections
                sectionToggles.forEach(otherToggle => {
                    if (otherToggle !== toggle) {
                        otherToggle.classList.remove('active');
                        const otherSection = otherToggle.getAttribute('data-section');
                        const otherContent = document.getElementById(otherSection + '-content');
                        if (otherContent) {
                            otherContent.classList.remove('active');
                        }
                    }
                });

                // Toggle current section
                if (isActive) {
                    toggle.classList.remove('active');
                    content.classList.remove('active');
                } else {
                    toggle.classList.add('active');
                    content.classList.add('active');
                }
            }
        });
    });

    // Close mobile menu when clicking on a menu item
    const mobileMenuItems = document.querySelectorAll('.mobile-menu-item');

    mobileMenuItems.forEach(function(item) {
        item.addEventListener('click', function() {
            if (this.href && !this.href.includes('#')) {
                setTimeout(closeMobileMenu, 100);
            }
        });
    });

    // Handle window resize
    window.addEventListener('resize', function() {
        if (window.innerWidth > 768) {
            closeMobileMenu();
        }
    });

    // Prevent scrolling on mobile when menu is open
    document.addEventListener('touchmove', function(e) {
        if (document.body.classList.contains('mobile-menu-open')) {
            if (!mobileMenuPanel.contains(e.target)) {
                e.preventDefault();
            }
        }
    }, { passive: false });

    console.log('Separate mobile navigation system initialized successfully');
});
</script>